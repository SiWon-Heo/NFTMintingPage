export const usePlanetContract = (web3: Web3 | null)=>{
  //web3이 값이 null일시 (지갑 설치 안됨 등) 에러리턴 하게 함수 설정
  if (!web3){
    return {
      contractAddress:CONTRACT_ADDRESS,
      //호출할 컨트렉트 함수 혹은 public 변수들 정의 
      //민트 함수는 트랜잭션 전송 시 파라미터(options)가 필요함, sendoptions 인터페이스를 가져와 파라미터로 받고 send 시 파라미터를 넘김
      mintPlanet: (optinons: SendOptions)=> {throw new Error('wallet not installed')},
      tokenURI: (tokenId:string)=>  {throw new Error('wallet not installed')},
      ownerOf: (tokenId:string)=>  {throw new Error('wallet not installed')},
      totalSupply: ()=>  {throw new Error('wallet not installed')},
  
    }
  }
  const contract = new web3.eth.Contract(contractAbi as any, CONTRACT_ADDRESS);

  return {
    contractAddress:CONTRACT_ADDRESS,
    //호출할 컨트렉트 함수 혹은 public 변수들 정의 
    //민트 함수는 트랜잭션 전송 시 파라미터(options)가 필요함, sendoptions 인터페이스를 가져와 파라미터로 받고 send 시 파라미터를 넘김
    mintPlanet: (optinons: SendOptions)=> contract.methods.mintPlanet().send(optinons),
    tokenURI: (tokenId:string)=> contract.methods.tokenURI(tokenId).call(),
    ownerOf: (tokenId:string)=> contract.methods.ownerOf(tokenId).call(),
    totalSupply: ()=> contract.methods.totalSupply().call(),

  }
}